

class Number1to10 {
    public static void main(String[] args) {
        
        
        
        
        int i,j,lines=5,n=1;
        
    
        for(i=1;i<=lines;i++){
            
            for(j=1;j<=i;j++){
                
                System.out.print(" "+n);
                  n++;
            }
            
             System.out.println("");
           
        }
        
    }
}
